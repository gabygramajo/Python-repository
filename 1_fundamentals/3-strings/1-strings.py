# Definición de cadenas usando comillas dobles triples:
cadena1 = """En Python es posible definir
 cadenas de caracteres utilizando más de una
 línea de código"""
print(f"cadena1 = {cadena1}") 
 
 # Definición de cadenas usando comillas simples triples:
cadena2 = '''Por supuesto, se puede hacer
 lo mismo utilizando comillas simples'''
print(f"\ncadena2 = {cadena2}")

# longitud - len
print(f"\n1) cadena1 len = {len(cadena1)}")
print(f"2) cadena2 len = {len(cadena2)}")


# comparison operator
cadena1 = "Hola"
cadena2 = "hola"
print(f"\n1) cadena1 = '{cadena1}' and cadena2 = '{cadena2}'")
print(f"2) cadena1 == cadena2 -> {cadena1 == cadena2}")
print(f"3) cadena1 < cadena2 -> {cadena1 < cadena2}")
print(f"4) cadena1 > cadena2 -> {cadena1 >= cadena2}")

